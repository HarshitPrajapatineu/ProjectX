private void setCustomerService(){
    dbConnect = new DBConnect();
    CustomerService custService = new CustomerService();
    
    
    
    try {
        ObjectSet result = dbConnect.queryByExample(custService);
        CustomerService custSer = new CustomerService();
            Object[] arr = result.toArray();
                for (Object o : arr) {
                    custSer = (CustomerService) o;
                   
                }
            if (result == null) {
         
                dbConnect.setEntity(custService);
            }
            
            Map<String, String> QA = new HashMap<>();
            QA = custSer.getQueriesWithAnswers();
            
            DefaultTableModel dtm = (DefaultTableModel) queriesTable.getModel();
            dtm.setRowCount(0);
            
            String[] values = QA.values().toArray(new String[0]);
            int len= values.length;
            for (int i=0;i<len;) {
                Object[] row = new Object[2];
                row[0] = values[i];
                row[1] = values[i+1];
                i=+2;
                dtm.addRow(row);
                
            }
            
            
            
            
        
        
    } catch (Exception e) {
            System.err.println(e.getMessage());
            System.err.println("Exception:" + e.getMessage());
            System.err.println("Exception:" + e.getStackTrace());
        }
    

}

